<script src="https://cdn.jsdelivr.net/npm/jquery-validation@1.19.3/dist/additional-methods.min.js"></script> 
<script>
jQuery( document ).ready(function() {
	jQuery("form[id='loginForm']").submit(function(e) {
        e.preventDefault();
    }).validate({
		// Specify validation rules
		rules: {
			email: {
				required: true,
				email: true
			},
			password: {
				required: true,
			}
		},
		// Specify validation error messages
		messages: {
			email: {
				required: 'Email address is required',
				email: 'Provide a valid Email address',
			},
			password: {
				required: 'Password is required',
				
			}
		},
		submitHandler: function(form) {
            var formdata = jQuery("form[id='loginForm']");
            var urls = formdata.prop('action');
            jQuery("#loginBtn").html('Submitting <i class="fa fa-spinner fa-spin"></i>');
            jQuery("#loginBtn").attr("disabled", true);
            jQuery.ajax({
                type: "POST",
                url: urls,
                data: formdata.serialize(), 
                success:function(data){  
                    if (data.api_response == 'Success')
                    {
                        location.href = data.data;  
                    } else if(data.api_response == 'error'){
                        console.log(data.message)
                        jQuery("#errorMessage").html(data.message);
                        jQuery("#loginBtn").html('Submit');
                        jQuery("#loginBtn").attr("disabled", false);
                    } 
                },
                error: function(xhr, status, error) 
                {
                    jQuery.each(xhr.responseJSON.errors, function (key, item) 
                    {
                        jQuery("#login" + key + "-error").html(item);
                    });
                }
            }); 
		}
	});
	jQuery("form[id='registerForm']").submit(function(e) {
        e.preventDefault();
    }).validate({
		// Specify validation rules
		rules: {
            first_name: {
                required: true,
                maxlength: 20,
                letterspaceonly: true
            },
            last_name: {
                required: true,
                maxlength: 20,
                letterspaceonly: true
            },
            email: {
                required: true,
                email: true
            },
            mobile: {
                required: true,
                number: true,
                maxlength: 12,
                minlength: 10
            },
            password : {
                required: true,
                minlength : 6,
                maxlength: 20,
            },
            password_confirmation : {
                required: true,
                equalTo : "#password"
            }
        },
        // Specify validation error messages
        messages: {
            first_name: {
                required: 'First name is required',
                maxlength: 'First name should be less than 20 characters',
                letterspaceonly: 'First name should contains letters only'
            },
            last_name: {
                required: 'Last name is required',
                maxlength: 'Last name should be less than 20 characters',
                letterspaceonly: 'Last name should contains letters only'
            },
            email: {
                required: 'Email field is required',
                email: 'Provide an valid email address'
            },
            mobile: {
                required: 'Mobile number is required',
                number: 'Please enter a valid digit',
                maxlength: 'Mobile number should be of 10-12 digit',
                minlength: 'Mobile number should be of 10-12 digit',
            },
            password: {
                required: 'Password field is required',
                minlength: 'Please enter minimum 6 character for password',
                maxlength: 'Please enter less than 20 character for password'
            },
            password_confirmation: {
                required: 'Confirm password field is required',
                equalTo: 'Confirm password should be same as password',
            }
		},
		submitHandler: function(form) {
            var formdata = jQuery("form[id='registerForm']");
            var urls = formdata.prop('action');
            jQuery("#registerBtn").html('Registering <i class="fa fa-spinner fa-spin"></i>');
            jQuery("#registerBtn").attr("disabled", true);
            jQuery.ajax({
                type: "POST",
                url: urls,
                data: formdata.serialize(), 
                success:function(data){ 
                    let result = JSON.parse(data);  
                    if (result.success == true)
                    {
                        location.href = result.message;  
                    } else if(result.success == false){
                        jQuery("#status").html('<div class="alert alert-danger  alert-dismissible hidden"><a href="#" class="close" data-dismiss="alert" aria-label="close">&times;</a>'+result.message+'</div>');
                        jQuery("#registerBtn").html('Register');
                        jQuery("#registerBtn").attr("disabled", false);
                    } 
                },
                error: function(xhr, status, error) 
                {
                    jQuery.each(xhr.responseJSON.errors, function (key, item) 
                    {
                        jQuery("#register" + key + "-error").html(item);
                    });
                }
            }); 
		}
	});
});
</script>
